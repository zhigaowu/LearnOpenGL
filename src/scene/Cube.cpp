        void Cube::MoveTo(const glm::vec3& position_, float rotation_angle, const glm::vec3& rotation_axes)
        {
            position = position_;
            rotation = glm::vec4(rotation_axes, rotation_angle);

            model = glm::rotate(glm::translate(glm::mat4(1.0f), position_), glm::radians(rotation_angle), rotation_axes);
        }

